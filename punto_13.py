first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))

addition = first_number + second_number
subtraction = first_number - second_number
multiplication = first_number * second_number
division = first_number / second_number

print("The addition of", first_number, "and", second_number, "is", addition)
print("The subtraction of", first_number, "and", second_number, "is", subtraction)
print("The multiplication of", first_number, "and", second_number, "is", multiplication)
print("The division of", first_number, "and", second_number, "is", division)
